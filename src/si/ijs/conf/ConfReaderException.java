package si.ijs.conf;

public class ConfReaderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -472202457089537142L;

	public ConfReaderException() {
	}

	public ConfReaderException(String arg0) {
		super(arg0);
	}

	public ConfReaderException(Throwable arg0) {
		super(arg0);
	}

	public ConfReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
